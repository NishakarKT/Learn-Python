num = 50

# If-Elif-Else Ladder
if(num > 10):
    print("num > 10")
elif(num > 20):
    print("num > 20")
elif(num > 30):
    print("num > 30")
elif(num > 40):
    print("num > 40")
else:
    print("num = 50")
# Only "one" of the condition codes (First true condition's code) is exexuted in an "If-Elif-Else ladder"
# "    " / tab is used for indentation that the line is "inside" the if/elif/else

# Multiple if statements
# now all the true condition codes will be executed
if(num > 10):
    print("num > 10")
if(num > 20):
    print("num > 20")
if(num > 30):
    print("num > 30")
if(num > 40):
    print("num > 40")
