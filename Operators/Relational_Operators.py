print("{0} > {1} = {2}".format(2, 3, 2 > 3))
print("{0} >= {1} = {2}".format(2, 3, 2 >= 3))
print("{0} < {1} = {2}".format(2, 3, 2 < 3))
print("{0} <= {1} = {2}".format(2, 3, 2 <= 3))
print("{0} == {1} = {2}".format(2, 3, 2 == 3))

# string comparision (Dictionary_Like / Lexicographic ordering)
print("{0} < {1} = {2}".format("break", "brake", "break" < "brake"))
print("{0} > {1} = {2}".format("break", "brake", "break" > "brake"))