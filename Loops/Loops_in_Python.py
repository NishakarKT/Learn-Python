### while loop
i = 1

while (i <= 10):
    # or while i<= 10 (no parenthesis)
    print(i)
    i = i+1

print("Printing the content of a list.")
list1 = [1, 2, 3, "Happy", "Birthday"]
i = 0
while(i < len(list1)):
    print(list1[i])
    i = i+1

### for loop
print("Printing the content of a list.")
for item in list1:
    print(item)
