# List is similar to arrays but NOT EXACTLY ARRAY as size of arrays are fixed but lists are dynamic.
# Lists can be combination of objects of different datatypes.
sample_list = [1, 2, 'hello']
print(sample_list)

###############################################################################################################
# accessing elements of the list just like arrays
# negative indices are supported in python and are useful when the length of a list is unknown.
print(sample_list[-4])
print(sample_list[-3])
print(sample_list[-2])
# -1 impiles the last element of the list and you can locate others accordingly
print(sample_list[-1])
print(sample_list[0])
print(sample_list[1])
print(sample_list[2])
print(sample_list[3])

